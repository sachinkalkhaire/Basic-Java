package Day5;

public class Jumping_statement4 {

	public static void main(String[] args) {

		//jumping statement: break,continue

//break
 for(int i=1;i<=100;i++)
 {
	 if(i==15)
	 {
		 break;
	 }
	 System.out.println(i);
 }
 
 //continue:
 
 for(int r=1;r<=20;r++)
 {
	 if(r==15)
	 {
		continue;
	 }
	 System.out.println(r);
 }
 
	}

}
